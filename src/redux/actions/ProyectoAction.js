import {
  GET_COLECCION_PROYECTO,
  GET_COLECCION_LIGERA_PROYECTO,
  SHOW_PROYECTO,
  UPDATE_PROYECTO,
  DELETE_PROYECTO,
  CREATE_PROYECTO,
  FETCH_ERROR,
  FETCH_START,
  FETCH_SUCCESS,
  SHOW_MESSAGE,
} from '../../shared/constants/ActionTypes';
import jwtAxios from '../../@crema/services/auth/jwt-auth/jwt-api';

import {appIntl} from '../../@crema/utility/Utils';

export const onGetColeccion = (
  currentPage,
  rowsPerPage,
  orderByToSend,
  solicitante,
  tipo,
  estado,
  fecha,
) => {
  const {messages} = appIntl();
  const page = currentPage ? currentPage : 0;
  const solicitanteAux = solicitante ? solicitante : '';
  const tipoAux = tipo ? tipo : '';
  const estadoAux = estado ? estado : '';
  const fechaAux = fecha ? fecha : '';
  const ordenar_por = orderByToSend ? orderByToSend : '';

  return (dispatch) => {
    dispatch({type: FETCH_START});
    jwtAxios
      .get('proyectos', {
        params: {
          page: page,
          limite: rowsPerPage,
          ordenar_por: ordenar_por,
          solicitante: solicitanteAux,
          tipo: tipoAux,
          estado: estadoAux,
          fecha: fechaAux,
        },
      })
      .then((data) => {
        if (data.status === 200) {
          dispatch({type: FETCH_SUCCESS});
          dispatch({type: GET_COLECCION_PROYECTO, payload: data});
        } else {
          dispatch({
            type: FETCH_ERROR,
            payload: messages['message.somethingWentWrong'],
          });
        }
      })
      .catch((error) => {
        dispatch({type: FETCH_ERROR, payload: error.message});
      });
  };
};

